//Dati due caratteri, restituire la somma dei loro valori ASCII

public class SumASCII {
    public static void main(String args[]) {
      char first = 'P';
      char second = 'A';
      
      int somma = (int)first + (int)second;
      
      System.out.println("Sum ASCII 1st and 2nd char  = " + somma);
    }
}